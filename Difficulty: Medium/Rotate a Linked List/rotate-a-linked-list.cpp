    Node* rotate(Node* head, int k) {
        // Your code here
        Node* c=new Node(0);
        Node* e=c;
        while(k>0 and head!=NULL)
        {
            Node* d=new Node(head->data);
            c->next=d;
            c=c->next;
            k-=1;
            head=head->next;
        }
        Node* a=head;
        while(head!=NULL and head->next!=NULL)
        {
            head=head->next;
        }
        if(head!=NULL)
        head->next=e->next;
        else
        a=e->next;
        return a;
        
    }
